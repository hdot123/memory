def generate_skill_showdoc_workflow_yaml(
    showdoc: ShowdocSyncConfig,
) -> str:
    """Generate ShowDoc sync skill workflow YAML template.

    Produces a YAML defining a ShowDoc sync skill with correct parameters.

    Returns an empty string when ``showdoc.enabled`` is ``False``.
    """
    if not showdoc.enabled:
        return ""

    item_id = showdoc.item_id
    core_files_yaml = ""
    if showdoc.core_files:
        core_files_lines = []
        for f in showdoc.core_files:
            core_files_lines.append(f'    - "{f}"')
        core_files_yaml = "\n".join(core_files_lines) + "\n"
    else:
        core_files_yaml = '    - "docs/**/*.md"\n    - "CHANGELOG.md"\n'

    return f"""# memory/system/skills/showdoc_sync_workflow.yaml
version: 1
workflow: showdoc-sync
rules:
  - ci_job: "sync-to-showdoc"
  - showdoc_item_id: {item_id}
  - require_pipeline_jobs: ["test", "health-check"]
  - branch: "main"

skills:
  - name: sync_to_showdoc
    goal: "Sync Markdown docs from GitLab to ShowDoc via Open API"
    precheck:
      - "adapter.toml [sync.showdoc] enabled=true"
      - "SHOWDOC_API_KEY and SHOWDOC_API_TOKEN env vars set"
      - "SHOWDOC_URL env var or adapter api_url configured"
    sync:
      - script: "scripts/sync_to_showdoc.py"
      - install: "pip install requests"
      - core_files:
{core_files_yaml}    - incremental: true
      - manifest: ".showdoc-manifest.json"
      - api_endpoint: "/server/index.php?s=/api/item/updateByApi"
      - retry: 3
      - backoff: "exponential (5s/15s/30s)"
"""


def generate_skill_memory_init_fill_yaml(project_name: str = "") -> str:
    """Generate memory-init-fill skill workflow YAML from template file.

    Reads the YAML template from ``workspace/templates/skills/memory-init-fill.yaml``
    and returns its content. This skill is static (no variable substitution needed)
    because it defines a workflow rather than project-specific configuration.

    Parameters
    ----------
    project_name:
        Optional project identifier (currently unused, reserved for future templating).

    Returns
    -------
    str
        The complete YAML string for the memory-init-fill skill workflow,
        or empty string if the template file is not found.
    """
    _repo_root = Path(__file__).resolve().parent.parent.parent
    _template_path = _repo_root / "workspace" / "templates" / "skills" / "memory-init-fill.yaml"

    if _template_path.is_file():
        return _template_path.read_text(encoding="utf-8")

    # Template not found — skill is optional, return empty
    return ""
