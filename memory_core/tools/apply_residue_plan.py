# Apply functions
# ---------------------------------------------------------------------------

def _apply_move_root_pollution(
    target: Path,
    action: dict[str, Any],
    dry_run: bool,
    reports_dir: Path,
) -> tuple[bool, str, dict[str, Any] | None]:
    """Apply move_root_pollution action.

    Returns (success, message, applied_action_dict).
    """
    src_rel = action.get("path", "")
    src_path = target / src_rel

    if not src_path.exists():
        return True, f"Source file does not exist, skipping: {src_rel}", None

    # Determine destination: artifacts/reports/<original-name>
    dst_rel = f"{DEFAULT_REPORTS_DEST}/{src_path.name}"
    dst_path = target / dst_rel

    # Check if destination already exists
    if dst_path.exists():
        return False, f"Destination already exists, refusing to overwrite: {dst_rel}", None

    if dry_run:
        return (
            True,
            f"Would move: {src_rel} -> {dst_rel}",
            {"action": "move_root_pollution", "src": src_rel, "dst": dst_rel, "status": "dry-run"},
        )

    # Ensure reports directory exists
    reports_dir.mkdir(parents=True, exist_ok=True)

    # Move the file
    try:
        shutil.move(str(src_path), str(dst_path))
        return (
            True,
            f"Moved: {src_rel} -> {dst_rel}",
            {"action": "move_root_pollution", "src": src_rel, "dst": dst_rel, "status": "applied"},
        )
    except Exception as e:
        return False, f"Failed to move {src_rel}: {e}", None


def _apply_ignore_runtime_artifact(
    target: Path,
    action: dict[str, Any],
    dry_run: bool,
) -> tuple[bool, str, dict[str, Any] | None]:
    """Apply ignore_runtime_artifact action.

    Currently only logs that it would be ignored, does not modify .gitignore.
    """
    path = action.get("path", "")

    if dry_run:
        return (
            True,
            f"Would ignore runtime artifact: {path}",
            {"action": "ignore_runtime_artifact", "path": path, "status": "dry-run"},
        )

    # Currently just log, don't modify .gitignore
    return (
        True,
        f"Ignored runtime artifact (no .gitignore modification): {path}",
        {"action": "ignore_runtime_artifact", "path": path, "status": "skipped"},
    )


def _apply_action(
    target: Path,
    action: dict[str, Any],
    dry_run: bool,
    reports_dir: Path,
) -> tuple[bool, str, dict[str, Any] | None]:
    """Apply a single action.

    Returns (success, message, applied_action_dict).
    """
    action_type = action.get("action", "")

    if action_type not in ALLOWED_AUTO_ACTIONS:
        return False, f"Action type not allowed: {action_type}", None

    if action_type == "move_root_pollution":
        return _apply_move_root_pollution(target, action, dry_run, reports_dir)
    elif action_type == "ignore_runtime_artifact":
        return _apply_ignore_runtime_artifact(target, action, dry_run)

    return False, f"Unknown action type: {action_type}", None


