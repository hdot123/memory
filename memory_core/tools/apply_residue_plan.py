# Rollback functions
# ---------------------------------------------------------------------------

def _load_backup_manifest(manifest_path: Path) -> BackupManifest:
    """Load backup manifest from disk."""
    data = json.loads(manifest_path.read_text(encoding="utf-8"))

    entries = [
        BackupEntry(
            action=e["action"],
            src=e["src"],
            dst=e.get("dst"),
            backup_path=e["backup_path"],
            sha256=e["sha256"],
            timestamp=e["timestamp"],
        )
        for e in data.get("entries", [])
    ]

    return BackupManifest(
        version=data.get("version", "backup-manifest-v1"),
        created_at=data.get("created_at", ""),
        target=data.get("target", ""),
        plan_path=data.get("plan_path"),
        entries=entries,
    )


def _rollback_from_manifest(
    target: Path,
    manifest_path: Path,
    dry_run: bool,
) -> ApplyResult:
    """Rollback changes from a backup manifest."""
    result = ApplyResult(
        success=True,
        target=str(target.resolve()),
        dry_run=dry_run,
    )

    try:
        manifest = _load_backup_manifest(manifest_path)
    except Exception as e:
        result.errors.append(f"Failed to load backup manifest: {e}")
        result.success = False
        return result

    backup_dir = manifest_path.parent

    for entry in manifest.entries:
        if entry.action != "move_root_pollution":
            result.warnings.append(f"Skipping rollback for unsupported action: {entry.action}")
            continue

        src_path = target / entry.src
        backup_full_path = backup_dir / entry.backup_path

        # Check for conflicts
        if src_path.exists():
            result.errors.append(f"Cannot rollback: source already exists (conflict): {entry.src}")
            result.success = False
            continue

        if not backup_full_path.exists():
            result.errors.append(f"Cannot rollback: backup file missing: {entry.backup_path}")
            result.success = False
            continue

        # Verify checksum
        current_sha256 = _sha256_file(backup_full_path)
        if current_sha256 != entry.sha256:
            result.errors.append(f"Cannot rollback: backup checksum mismatch: {entry.backup_path}")
            result.success = False
            continue

        if dry_run:
            result.actions_applied.append({
                "action": "rollback",
                "src": entry.backup_path,
                "dst": entry.src,
                "status": "dry-run",
            })
            continue

        # Perform rollback
        try:
            src_path.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(backup_full_path, src_path)

            # Remove the applied destination if it exists (artifacts/reports/...)
            dst_path = target / f"{DEFAULT_REPORTS_DEST}/{src_path.name}"
            if dst_path.exists():
                dst_path.unlink()

            result.actions_applied.append({
                "action": "rollback",
                "src": entry.backup_path,
                "dst": entry.src,
                "status": "applied",
            })
        except Exception as e:
            result.errors.append(f"Failed to rollback {entry.src}: {e}")
            result.success = False

    return result


