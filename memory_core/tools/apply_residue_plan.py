#!/usr/bin/env python3
"""Safe migrator for applying residue cleanup plans with backup/rollback support.

Usage:
    memory-apply-residue-plan --target /path/to/project --plan plan.json
    memory-apply-residue-plan --target /path/to/project --plan plan.json --dry-run
    memory-apply-residue-plan --target /path/to/project --rollback manifest.json

This tool safely applies migration plans generated by memory-plan-residue.
It implements strict safety rules and requires explicit backups before any
modifications.

Safety guarantees:
    - No plan = rejection (must have valid plan.json)
    - Dry-run mode makes no changes
    - Non-dry-run requires backup manifest creation
    - Human-confirmation plans rejected by default
    - Manual-decision actions rejected by default
    - Forbidden overwrites blocked (AGENTS.md, INDEX.md, project-map/**, CLAUDE.md)
    - Only low-risk auto actions allowed: move_root_pollution, ignore_runtime_artifact
"""
from __future__ import annotations

import argparse
import hashlib
import json
import logging
import shutil
import sys
from dataclasses import dataclass, field
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

from memory_core.ownership import (
    Owned,
    classify_owned_path,
    load_memory_ownership,
)

# Setup logging
logging.basicConfig(level=logging.INFO, format="%(levelname)s: %(message)s")
logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

# Step 2.9: FORBIDDEN_OVERWRITE_PATTERNS replaced with classify_owned_path()
# Legacy patterns kept for reference during transition
LEGACY_FORBIDDEN_OVERWRITE_PATTERNS = [
    "AGENTS.md",
    "INDEX.md",
    "project-map/**",
    "CLAUDE.md",
]

# Forbidden files/patterns that must never be overwritten
# Now dynamically populated via classify_owned_path()
FORBIDDEN_OVERWRITE_PATTERNS = []  # type: list[str]

# Allowed low-risk auto actions
ALLOWED_AUTO_ACTIONS = {
    "move_root_pollution",
    "ignore_runtime_artifact",
}

# Default reports destination for move_root_pollution
DEFAULT_REPORTS_DEST = "memory/artifacts/reports"


# ---------------------------------------------------------------------------
# Data classes
# ---------------------------------------------------------------------------

@dataclass
class ApplyResult:
    """Result of applying a migration plan."""

    success: bool
    target: str
    dry_run: bool
    actions_applied: list[dict[str, Any]] = field(default_factory=list)
    actions_skipped: list[dict[str, Any]] = field(default_factory=list)
    actions_failed: list[dict[str, Any]] = field(default_factory=list)
    backup_manifest: dict[str, Any] | None = None
    errors: list[str] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)

    def to_dict(self) -> dict[str, Any]:
        return {
            "success": self.success,
            "target": self.target,
            "dry_run": self.dry_run,
            "actions_applied": self.actions_applied,
            "actions_skipped": self.actions_skipped,
            "actions_failed": self.actions_failed,
            "backup_manifest": self.backup_manifest,
            "errors": self.errors,
            "warnings": self.warnings,
            "summary": {
                "applied": len(self.actions_applied),
                "skipped": len(self.actions_skipped),
                "failed": len(self.actions_failed),
                "total": len(self.actions_applied) + len(self.actions_skipped) + len(self.actions_failed),
            },
        }


@dataclass
class BackupEntry:
    """Single backup entry for a file."""

    action: str
    src: str
    dst: str | None
    backup_path: str
    sha256: str
    timestamp: str

    def to_dict(self) -> dict[str, Any]:
        return {
            "action": self.action,
            "src": self.src,
            "dst": self.dst,
            "backup_path": self.backup_path,
            "sha256": self.sha256,
            "timestamp": self.timestamp,
        }


@dataclass
class BackupManifest:
    """Complete backup manifest."""

    version: str
    created_at: str
    target: str
    plan_path: str | None
    entries: list[BackupEntry]

    def to_dict(self) -> dict[str, Any]:
        return {
            "version": self.version,
            "created_at": self.created_at,
            "target": self.target,
            "plan_path": self.plan_path,
            "entries": [e.to_dict() for e in self.entries],
        }


# ---------------------------------------------------------------------------
# Utility functions
# ---------------------------------------------------------------------------

def _sha256_file(path: Path) -> str:
    """Calculate SHA256 hash of a file."""
    h = hashlib.sha256()
    with open(path, "rb") as f:
        for chunk in iter(lambda: f.read(8192), b""):
            h.update(chunk)
    return h.hexdigest()


def _is_forbidden_path(path: str, target: Path | None = None) -> bool:
    """Step 2.9: Check if a path is forbidden using classify_owned_path.

    Args:
        path: Relative path to check
        target: Project root (optional, for ownership lookup)

    Returns:
        True if path is owned/protected
    """
    # Use classify_owned_path if target is available
    if target is not None:
        try:
            ownership = load_memory_ownership(target)
            classification = classify_owned_path(path, ownership=ownership)
            return isinstance(classification, Owned)
        except Exception:
            pass  # Fall through to legacy check

    # Legacy fallback
    path_lower = path.lower()
    forbidden = ["agents.md", "index.md", "claude.md"]
    if any(f in path_lower for f in forbidden):
        return True
    if "project-map" in path_lower:
        return True
    return False


def _matches_pattern(path: str, pattern: str) -> bool:
    """Simple pattern matching for forbidden paths."""
    import fnmatch
    return fnmatch.fnmatch(path, pattern) or fnmatch.fnmatch(path.lower(), pattern.lower())


def _now_iso() -> str:
    """Current ISO timestamp."""
    return datetime.now(timezone.utc).isoformat()


# ---------------------------------------------------------------------------
# Validation functions
# ---------------------------------------------------------------------------

def _validate_plan(plan: dict[str, Any], target: Path | None = None) -> tuple[bool, list[str]]:
    """Validate migration plan structure.

    Args:
        plan: Plan dictionary to validate
        target: Project root path (optional, for ownership checks)

    Returns (is_valid, error_messages).
    """
    errors: list[str] = []

    if not isinstance(plan, dict):
        errors.append("Plan must be a JSON object")
        return False, errors

    # Check required fields
    required_fields = ["target", "actions", "risk_level", "requires_human_confirmation"]
    for required_field in required_fields:
        if required_field not in plan:
            errors.append(f"Missing required field: {required_field}")

    # Check requires_human_confirmation
    if plan.get("requires_human_confirmation") is True:
        errors.append("Plan requires human confirmation (requires_human_confirmation=true)")

    # Step 2.9: Initialize forbidden patterns from classify_owned_path if target available
    forbidden_paths: set[str] = set()
    if target is not None:
        try:
            ownership = load_memory_ownership(target)
            # Check all files under target
            if target.exists():
                for item in target.rglob("*"):
                    if item.is_file():
                        try:
                            rel_path = item.relative_to(target).as_posix()
                            classification = classify_owned_path(rel_path, ownership=ownership)
                            if isinstance(classification, Owned):
                                forbidden_paths.add(rel_path.lower())
                        except (ValueError, OSError):
                            pass
        except Exception:
            pass  # Fall back to legacy patterns

    # Check actions
    actions = plan.get("actions", [])
    if not isinstance(actions, list):
        errors.append("actions must be a list")
        return False, errors

    for i, action in enumerate(actions):
        if not isinstance(action, dict):
            errors.append(f"Action {i} must be an object")
            continue

        action_type = action.get("action", "")

        # Reject manual_decision_required actions
        if action_type == "manual_decision_required":
            errors.append(f"Action {i} requires manual decision: {action.get('path', 'unknown')}")

        # Step 2.9: Check forbidden overwrites using classify_owned_path
        path = action.get("path", "")
        if path:
            # Check against dynamically loaded owned paths
            if path.lower() in forbidden_paths:
                errors.append(f"Action {i} targets owned/forbidden path: {path}")
            # Also use _is_forbidden_path as fallback
            elif _is_forbidden_path(path, target=target):
                errors.append(f"Action {i} targets forbidden path: {path}")

    return len(errors) == 0, errors


# ---------------------------------------------------------------------------
# Backup functions
# ---------------------------------------------------------------------------

def _create_backup(
    target: Path,
    plan_path: Path | None,
    actions: list[dict[str, Any]],
    backup_dir: Path,
) -> BackupManifest:
    """Create backup for files that will be modified.

    Only backs up files for move_root_pollution actions.
    """
    entries: list[BackupEntry] = []
    timestamp = _now_iso()

    for action in actions:
        action_type = action.get("action", "")

        # Only backup for move_root_pollution
        if action_type != "move_root_pollution":
            continue

        src_path = target / action.get("path", "")
        if not src_path.exists():
            continue

        # Calculate checksum
        sha256 = _sha256_file(src_path)

        # Create backup file
        backup_file = backup_dir / f"{src_path.name}.backup"
        backup_file.parent.mkdir(parents=True, exist_ok=True)
        shutil.copy2(src_path, backup_file)

        entry = BackupEntry(
            action=action_type,
            src=str(src_path.relative_to(target)),
            dst=action.get("target_bucket"),  # This is the conceptual bucket, actual dst calculated later
            backup_path=str(backup_file.relative_to(backup_dir)),
            sha256=sha256,
            timestamp=timestamp,
        )
        entries.append(entry)

    return BackupManifest(
        version="backup-manifest-v1",
        created_at=timestamp,
        target=str(target.resolve()),
        plan_path=str(plan_path) if plan_path else None,
        entries=entries,
    )


def _write_manifest(manifest: BackupManifest, backup_dir: Path) -> Path:
    """Write backup manifest to disk."""
    manifest_path = backup_dir / "manifest.json"
    manifest_path.write_text(
        json.dumps(manifest.to_dict(), indent=2, ensure_ascii=False),
        encoding="utf-8",
    )
    return manifest_path


