def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        description="Safe migrator for applying residue cleanup plans with backup/rollback support."
    )
    parser.add_argument(
        "--target",
        type=Path,
        required=True,
        help="Path to the target project root.",
    )
    parser.add_argument(
        "--plan",
        type=Path,
        default=None,
        help="Path to the migration plan JSON file.",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Only report what would be done without making changes.",
    )
    parser.add_argument(
        "--backup-dir",
        type=Path,
        default=None,
        help="Directory for backups (default: memory/system/backups/residue-<timestamp>).",
    )
    parser.add_argument(
        "--rollback",
        type=Path,
        default=None,
        help="Path to backup manifest JSON for rollback.",
    )
    parser.add_argument(
        "--json",
        action="store_true",
        help="Output results as JSON.",
    )

    args = parser.parse_args(argv)

    # Validate mutually exclusive options
    if args.plan and args.rollback:
        print("Error: --plan and --rollback are mutually exclusive.", file=sys.stderr)
        return 2

    if args.rollback:
        # Rollback mode
        result = _rollback_from_manifest(args.target, args.rollback, args.dry_run)
    else:
        # Apply mode
        result = apply_residue_plan(
            args.target,
            args.plan,
            args.dry_run,
            args.backup_dir,
            args.json,
        )

    # Output
    if args.json:
        print(json.dumps(result.to_dict(), indent=2, ensure_ascii=False))
    else:
        print("=" * 60)
        print("Residue Plan Application Report")
        print("=" * 60)
        print(f"Target: {result.target}")
        print(f"Mode: {'DRY-RUN' if result.dry_run else 'APPLY'}")
        print("-" * 60)

        if result.backup_manifest:
            print(f"Backup manifest: {result.backup_manifest['path']}")
            print(f"  Entries: {result.backup_manifest['entry_count']}")
            print("-" * 60)

        for action in result.actions_applied:
            status = action.get("status", "applied")
            if action.get("action") == "rollback":
                print(f"  [ROLLBACK] {action.get('src')} -> {action.get('dst')} ({status})")
            elif "dst" in action:
                print(f"  [APPLY] {action.get('src')} -> {action.get('dst')} ({status})")
            else:
                print(f"  [APPLY] {action.get('action')} {action.get('path')} ({status})")

        for action in result.actions_skipped:
            print(f"  [SKIP] {action.get('action')} {action.get('path')}: {action.get('reason')}")

        for action in result.actions_failed:
            print(f"  [FAIL] {action.get('action')} {action.get('path')}: {action.get('error')}")

        for warning in result.warnings:
            print(f"  [WARN] {warning}")

        for error in result.errors:
            print(f"  [ERROR] {error}")

        print("-" * 60)
        summary = result.to_dict()["summary"]
        print(f"Summary: {summary['applied']} applied, {summary['skipped']} skipped, {summary['failed']} failed")
        status = "SUCCESS" if result.success else "FAILED"
        print(f"Status: {status}")
        print("=" * 60)

    return 0 if result.success else 1


if __name__ == "__main__":
    raise SystemExit(main())
